// FarziPedia result page — render the structured blog from chrome.storage.

const $ = (id) => document.getElementById(id);

function fmtTs(s) {
  s = Math.max(0, Math.round(s));
  const m = Math.floor(s / 60);
  return `${m}:${(s % 60).toString().padStart(2, '0')}`;
}

function frameSrc(frames, ts) {
  if (ts == null || !frames) return null;
  const tsInt = Math.round(ts);
  if (frames[tsInt]) return `data:image/jpeg;base64,${frames[tsInt]}`;
  // Find nearest within ±4s
  let best = null, bestDelta = Infinity;
  for (const k of Object.keys(frames)) {
    const d = Math.abs(parseInt(k) - tsInt);
    if (d < bestDelta) { bestDelta = d; best = k; }
  }
  return (best != null && bestDelta <= 4) ? `data:image/jpeg;base64,${frames[best]}` : null;
}

function el(tag, attrs = {}, children = []) {
  const e = document.createElement(tag);
  for (const [k, v] of Object.entries(attrs)) {
    if (k === 'class') e.className = v;
    else if (k === 'html') e.innerHTML = v;
    else if (k === 'text') e.textContent = v;
    else if (k.startsWith('on')) e.addEventListener(k.slice(2), v);
    else e.setAttribute(k, v);
  }
  for (const c of [].concat(children)) {
    if (c == null || c === false) continue;
    e.appendChild(typeof c === 'string' ? document.createTextNode(c) : c);
  }
  return e;
}

function renderBlock(block, frames) {
  switch (block.type) {
    case 'paragraph':
      return el('p', { text: block.text || '' });

    case 'image': {
      const src = frameSrc(frames, block.timestamp);
      if (!src) return null;
      const cap = el('figcaption', {}, [
        el('span', { class: 'ts', text: `[${fmtTs(block.timestamp)}]` }),
        block.caption || '',
      ]);
      return el('figure', {}, [
        el('img', { src, alt: block.caption || '' }),
        cap,
      ]);
    }

    case 'callout': {
      const kind = ['key', 'warning', 'aside'].includes(block.kind) ? block.kind : 'key';
      return el('aside', { class: `callout ${kind}` }, [
        el('p', { text: block.text || '' }),
      ]);
    }

    case 'code':
      return el('pre', {}, [el('code', { text: block.text || '' })]);

    case 'quote':
      return el('blockquote', {}, [
        el('p', { text: `"${block.text || ''}"` }),
        block.timestamp != null ? el('footer', { text: `— at ${fmtTs(block.timestamp)}` }) : null,
      ]);

    default:
      return null;
  }
}

function renderBlog(blog, frames) {
  const root = $('content');
  root.innerHTML = '';

  // Hero
  const hero = frameSrc(frames, blog.hero_timestamp);
  if (hero) {
    root.appendChild(el('div', { class: 'hero-figure' }, [
      el('img', { src: hero, alt: '' }),
    ]));
  }

  // Header
  const header = el('header', { class: 'codex-header' });
  const channel = blog.meta?.channel || '';
  const readMin = blog.estimated_read_minutes;
  header.appendChild(el('p', { class: 'label' }, [
    channel || 'Annotated transcription',
    readMin ? ` · ${readMin} min read` : '',
  ]));
  header.appendChild(el('h1', { text: blog.title || 'Untitled' }));
  if (blog.subtitle) header.appendChild(el('p', { class: 'subtitle', text: blog.subtitle }));
  if (blog.meta?.url) {
    const meta = el('div', { class: 'meta-row' }, [
      el('a', { href: blog.meta.url, target: '_blank', rel: 'noopener', text: 'Watch on YouTube' }),
      blog.meta.durationSeconds ? el('span', { class: 'dot', text: '·' }) : null,
      blog.meta.durationSeconds ? document.createTextNode(fmtTs(blog.meta.durationSeconds)) : null,
    ]);
    header.appendChild(meta);
  }
  header.appendChild(el('div', { class: 'ornament-rule' }, [el('span', { class: 'fleuron', text: '❦' })]));
  root.appendChild(header);

  // Sections
  for (const section of (blog.sections || [])) {
    const sec = el('section', { class: 'codex-section' });
    if (section.heading) sec.appendChild(el('h2', { text: section.heading }));
    for (const block of (section.blocks || [])) {
      const node = renderBlock(block, frames);
      if (node) sec.appendChild(node);
    }
    root.appendChild(sec);
  }

  // Takeaways
  if (blog.key_takeaways?.length) {
    const t = el('section', { class: 'takeaways' }, [el('h2', { text: 'Key takeaways' })]);
    const ul = el('ul');
    for (const kt of blog.key_takeaways) {
      ul.appendChild(el('li', {}, [document.createTextNode(kt)]));
    }
    t.appendChild(ul);
    root.appendChild(t);
  }

  // Colophon
  root.appendChild(el('footer', { class: 'colophon' }, [
    `Generated by FarziPedia · ${blog.meta?.nFrames || 0} frames analyzed`,
  ]));

  document.title = `${blog.title} · FarziPedia`;
}

// ---------- Markdown export ----------

function blogToMarkdown(blog, frames) {
  const lines = [];
  lines.push(`# ${blog.title || 'Untitled'}\n`);
  if (blog.subtitle) lines.push(`> ${blog.subtitle}\n`);
  if (blog.meta?.url) {
    const ch = blog.meta.channel || '';
    const dur = blog.meta.durationSeconds ? ` · ${fmtTs(blog.meta.durationSeconds)}` : '';
    lines.push(`${ch ? ch + ' — ' : ''}[Watch on YouTube](${blog.meta.url})${dur}\n`);
  }
  lines.push('---\n');
  for (const section of (blog.sections || [])) {
    if (section.heading) lines.push(`## ${section.heading}\n`);
    for (const block of (section.blocks || [])) {
      switch (block.type) {
        case 'paragraph':
          lines.push(`${block.text || ''}\n`); break;
        case 'image': {
          const src = frameSrc(frames, block.timestamp);
          if (src) {
            lines.push(`![${block.caption || ''}](${src})`);
            if (block.caption) lines.push(`*[${fmtTs(block.timestamp)}] ${block.caption}*\n`);
            else lines.push('');
          }
          break;
        }
        case 'callout':
          lines.push(`> **${(block.kind || 'key').toUpperCase()}** — ${block.text || ''}\n`); break;
        case 'code':
          lines.push('```' + (block.language || ''));
          lines.push(block.text || '');
          lines.push('```\n');
          break;
        case 'quote':
          lines.push(`> "${block.text || ''}"${block.timestamp != null ? ` — at ${fmtTs(block.timestamp)}` : ''}\n`);
          break;
      }
    }
  }
  if (blog.key_takeaways?.length) {
    lines.push('## Key takeaways\n');
    for (const kt of blog.key_takeaways) lines.push(`- ${kt}`);
    lines.push('');
  }
  lines.push(`\n---\n*Generated by FarziPedia from ${blog.meta?.url || 'a YouTube video'}.*\n`);
  return lines.join('\n');
}

function slugify(s, fallback = 'codex') {
  const cleaned = (s || '').replace(/[^A-Za-z0-9 \-_]/g, '').trim().replace(/\s+/g, '-').toLowerCase();
  return cleaned.slice(0, 80) || fallback;
}

function downloadText(text, filename, mime = 'text/markdown') {
  const blob = new Blob([text], { type: `${mime};charset=utf-8` });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  a.remove();
  URL.revokeObjectURL(url);
}

// ---------- Boot ----------

(async () => {
  const params = new URLSearchParams(location.search);
  const jobId = params.get('job');
  if (!jobId) {
    $('content').innerHTML = '<p class="loading">No job id.</p>';
    return;
  }
  const stored = await chrome.storage.local.get(jobId);
  const job = stored[jobId];
  if (!job) {
    $('content').innerHTML = '<p class="loading">Job not found in storage. Open the extension popup and run again.</p>';
    return;
  }
  const { blog, frames } = job;

  renderBlog(blog, frames);

  $('md-btn').addEventListener('click', () => {
    const md = blogToMarkdown(blog, frames);
    downloadText(md, `${slugify(blog.title)}.md`);
  });
  $('pdf-btn').addEventListener('click', () => window.print());
})();
